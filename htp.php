            <div class="game-img">
                <img src="img/slide-puzzel.png" alt="slide-puzzel.png">
            </div>
            <div class="game-content">
                <p class="game-rule">Slide Puzzle is a puzzle game that consists
                    of a frame of numbered square tiles in random order with one
                    tile missing. The player must slide the tiles into the
                    correct order to form a certain pattern, usually a
                    picture.</p>
                <ol>
                    <li>You can only move a tile into the empty space adjacent
                        to it—either horizontally or vertically, not
                        diagonally.</li>
                    <li>To move a piece, click or tap on the piece you want to
                        move. It will then slide into the empty space if there
                        is one adjacent to it.</li>
                    <li>The goal is to restore the original image by sliding the
                        pieces around until each piece is in its correct
                        position.</li>
                    <li>Once you successfully rearrange all the pieces to form
                        the complete image, you've solved the puzzle.</li>
                </ol>
                <p class="game-rule">For more details and rules, you can visit:
